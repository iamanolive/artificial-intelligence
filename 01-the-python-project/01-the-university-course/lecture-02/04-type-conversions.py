# integer to floating point conversions
print(float(99) + 100)
i = 42
print(type(i)) # integer
f = float(i)
print(f)
print(type(f)) # float


# floating-point results
print(10 / 2)
print(9 / 2) # 4.5
print(99 / 100)
print(10.0 / 2.0)
print(99.0 / 100.0)


# string conversions
sval = "123"
print(type(sval))
ival = int(sval)
print(type(ival))
print(ival + 1)